// Функція пошуку за місяцем і днем
SunriseTime getSunrise(uint8_t month, uint8_t day, int* dayNumber = nullptr) {
    SunriseTime s;
    for (int i = 0; i < sizeof(sunriseTable) / sizeof(SunriseTime); i++) {
      memcpy_P(&s, &sunriseTable[i], sizeof(SunriseTime));
      if (s.month == month && s.day == day) {
        if (dayNumber) {
            *dayNumber = i;  // зберігаємо індекс
        }
        return s;
      }
    }
  
    // Якщо не знайдено — значення за замовчуванням
    if (dayNumber) *dayNumber = -1;
    return {month, day, 0, 0};
}
  